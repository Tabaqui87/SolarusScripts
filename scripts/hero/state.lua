local state = {}

---------------------------------------------------------------------------------------------------
-- Metafields
---------------------------------------------------------------------------------------------------

state.__index = state

---------------------------------------------------------------------------------------------------
-- Metamethods
---------------------------------------------------------------------------------------------------

function state:__call(...)
  local o = sol.state.create()
  for k, v in pairs(self) do
    if k:sub(1, 2) ~= "__" then
      o[k] = v
    end
  end
  if o.initialize then
    o:initialize(...)
  end
  return o
end

---------------------------------------------------------------------------------------------------
-- Inheritance
---------------------------------------------------------------------------------------------------

function state:subclass()
  local c = {}
  for k, v in pairs(self) do
    if k:sub(1, 2) == "__" and k ~= "__index" then
      c[k] = v
    end
  end
  c.__index = c
  return setmetatable(c, self)
end

---------------------------------------------------------------------------------------------------
-- Constructor
---------------------------------------------------------------------------------------------------

function state:initialize(description)
  self:set_description(description)
end

return state